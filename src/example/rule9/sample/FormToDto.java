package example.rule9.sample;

class FormToDto {

    Dto convertFromFormToDto(Form form) {
        Dto dto = new Dto();

        dto.setFirstName(form.getFirstName());
        dto.setLastName(form.getLastName());
        dto.setFullName(form.getLastName() + " " + form.getFirstName());
        dto.setAge(Integer.parseInt(form.getAge()));
        dto.setCountry(form.getCountry());
        dto.setCity(form.getCity());
        dto.setZip(form.getZip());
        dto.setAddressLine(form.getAddressLine());
        dto.setAddress(form.getZip() + " " + form.getCity() + " " + form.getAddressLine());
        dto.setGender(form.getGender());
        dto.setHomePhone(form.getHomePhone());
        dto.setCellPhone(form.getCellPhone());

        return dto;
    }

}

